// Exercise 7: Swap Values
// Create two variables: x = 10 and y = 20.
// Swap their values using a third temporary variable.
// Log x and y after swapping.
let x = 10;
let y = 20;
let temp = x;
x = y;
y = temp;
console.log(x);
console.log(y)

